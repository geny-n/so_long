#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*d_str;

	i = 0;
	d_str = (char *)str;
	while (i < n)
	{
		d_str[i] = c;
		i++;
	}
	return (d_str);
}
